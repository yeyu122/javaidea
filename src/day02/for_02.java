package day2;

public class for_02 {
    public static void main(String[] args) {
        for (int i=0;i<=100;i++){
            System.out.println(i);
        }
        for (int i=100;i>0;i--){
            System.out.println(i);
        }
        for (int i=8;i<=8888;i=i*10+8){
            System.out.println(i);
        }
    }
}
